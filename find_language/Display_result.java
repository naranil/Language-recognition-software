    private void openButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openButtonActionPerformed
        // Action performed by the Open button: 
        // The button is open a jFilechooser
  		try {
		   UIManager.setLookAndFeel(UIManager.getSystemLookAndFeelClassName());
		}
		catch (Exception e) { // ignore exceptions and continue
		}
	   Scanner lineScanner = Display_result.selectTextFile();
		if (lineScanner!=null) {
                   String text = "";
                   
		   while (lineScanner.hasNextLine()) {
                      text = text + lineScanner.nextLine();        
		   }
                   
                languageLabel.setText(Find_language.findlanguage(text));
                photoLabel.setIcon(Find_language.flag(Find_language.findlanguage(text)));
                errorLabel.setText(Integer.toString((int) Math.round(Find_language.error_min(text)*100))+" %");
		}

    }//GEN-LAST:event_openButtonActionPerformed

